#include <stdio.h>

void	ft_inc(int *ptr)
{
	(*ptr)--;
}

int	main(void)
{
	int	n;	

	n = 5;
	printf("Before %d\n", n);
	ft_inc(&n);
	printf("After %d\n", n);
	return(0);
}
